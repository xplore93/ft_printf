char	*ft_strchr(const char *s, int c)
{
	while (*s >= 0)
	{
		if (*s == c)
			return ((char*)s);
		if (!*s && *s == c)
			return ((char*)s);
		if (!*s)
			break ;
		s++;
	}
	return (0);
}
